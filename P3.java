//3. Write a Java program to print the sum (addition), multiply,
//subtract, divide and remainder of two numbers arithmetic
//operation will be of user choice. 


class P3
{
	public static void main(String args[])
	{
		int a,b;
		
		a = 15;
		b = 2;
		
		System.out.println("sum of two number  : " + (a+b));
		System.out.println("sub of two number : " + (a-b));
		System.out.println("multi of two number : " + (a*b));
		System.out.println("div of two number : " + (a/b));
		System.out.println("remainder : " + (a % b));	
	}
}