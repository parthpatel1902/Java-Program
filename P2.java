// 2. Write a Java program to accept a number and check the number
// is even or not. Prints 1 if the number is even or 0 if the number is
// odd. 

class P2
{
	public static void main(String args[])
	{
		int n = 2;
		
		if(n % 2 == 0)
		{
			System.out.println("Number Is even");
		}
		else
		{
			System.out.println("Number Is odd");
		}	
	}
}